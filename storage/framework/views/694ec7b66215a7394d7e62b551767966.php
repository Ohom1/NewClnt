        const animateShipmentCards = () => {
            const cards = document.querySelectorAll('.shipment-card');
            let currentIndex = 0;
            const totalCards = cards.length;
            
            // Define the animation sequence
            const showNextCard = () => {
                // Hide current card with fadeOut animation
                if (currentIndex > 0) {
                    const currentCard = document.querySelector(`.shipment-card[data-index="${currentIndex-1}"]`);
                    currentCard.classList.add('animate__fadeOutDown');
                    
                    // After animation completes, hide the card
                    setTimeout(() => {
